    private void BtnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnLoginActionPerformed
       String username = TxtUsername.getText();
       String password = TxtPassword.getText();
        System.out.println("username: "+ username + ", password: "+ password);
        //Validar si los campo estan vacios
        if (username.isEmpty() || password.isEmpty())
            JOptionPane.showMessageDialog(null, "Faltan campos por diligenciar", "Login", JOptionPane.WARNING_MESSAGE);
        else{
            if(username.equals("MisionTIC") && password.equals("Ciclo2")){
                JOptionPane.showMessageDialog(null,"Bienvenido al Sistema", "Usuario" +  username, JOptionPane.INFORMATION_MESSAGE);
                //Instancia del JFRAME UserMenu
                UserMenu userMenu = new UserMenu();
                userMenu.setVisible(true);
                //Cerramos ell JFRAME  del login
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "No se ha podido iniciar sesion", username, JOptionPane.ERROR_MESSAGE);
            }
                
        }
    }//GEN-LAST:event_BtnLoginActionPerformed

